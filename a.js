/**
 * 1️⃣ 자바스크립트의 기본 문제
 *
 * - 자바스크립트의 변수는 기본적으로 전역 스코프(global scope)를 가짐
 * - 여러 파일에서 동일한 이름의 변수를 선언하면 충돌이 발생할 수 있음
 */

/* =========================================================
   2️⃣ 기존 방식 : 객체를 이용한 네임스페이스 관리
========================================================= */

// a.js
// var num = 10;  // 전역 변수 (충돌 가능)

var a = {
    num: 10,
};

a.num; // 10

// b.js
// var num = 20;  // 전역 변수 (충돌 가능)

var b = {
    num: 20,
};

b.num; // 20

/**
 * 객체로 네임스페이스를 만들면
 *
 * a.num
 * b.num
 *
 * 처럼 접근하기 때문에 변수 충돌을 방지할 수 있음
 */

/* =========================================================
   3️⃣ 네임스페이스 객체 사용 시 문제점
========================================================= */

/**
 * - 네임스페이스 객체가 여전히 전역에 존재
 * - 파일 간 의존성이 생김
 *
 * 예시
 * a.js에서 b.js 객체를 참조하면
 *
 * <script src="b.js"></script>
 * <script src="a.js"></script>
 *
 * 처럼 반드시 로드 순서를 맞춰야 함
 */

/* =========================================================
   4️⃣ ES6 모듈 시스템 등장
========================================================= */

/**
 * ES6에서 공식 모듈 시스템 도입
 *
 * 핵심 문법
 * - export : 외부로 내보내기
 * - import : 다른 파일에서 가져오기
 */

// a.js
export const num = 10;
console.log(num); // 10

// b.js
import { num } from "./a.js";
console.log(num); // 10

/**
 * ES6 모듈의 장점
 *
 * - 파일 단위 모듈 관리
 * - 필요한 값만 export/import 가능
 * - 전역 변수 오염 방지
 */

/* =========================================================
   5️⃣ 하지만 또 다른 문제 발생
========================================================= */

/**
 * 브라우저는 각 JS 파일을 각각 다운로드해야 함
 *
 * 예시
 *
 * JS 파일 100개
 * → HTTP 요청 100번 발생
 *
 * 또한
 * - 구형 브라우저는 ES6 Modules 미지원
 */

/* =========================================================
   6️⃣ 해결 방법 : 번들러(Bundler)
========================================================= */

/**
 * 대표적인 번들러
 *
 * - Webpack
 * - Rollup
 * - Parcel
 *
 * 역할
 * 여러 JS 모듈을 하나의 파일로 묶어서 배포
 */

/* =========================================================
   7️⃣ Webpack의 주요 역할
========================================================= */

/**
 * 1. 모듈 번들링
 *    여러 JS 파일을 하나의 파일로 묶음
 *
 * 2. 코드 스플리팅
 *    필요한 코드만 로드하여 초기 로딩 속도 개선
 *
 * 3. 트랜스파일링
 *    최신 JS 문법 → 구형 브라우저에서도 동작하도록 변환
 *    (ex: Babel)
 *
 * 4. 다양한 파일 처리
 *    Loader / Plugin을 통해
 *    - CSS
 *    - Image
 *    - Font
 *    등 다양한 리소스를 모듈로 관리 가능
 */
